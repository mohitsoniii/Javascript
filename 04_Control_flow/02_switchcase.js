// Switch Case is used in place of large number of Else if statements.

const month = 2

switch (month) {
    case 1:
        console.log("Month is Jan");
        break;
    case 2:
        console.log("Month is Feb");
        break;
    case 3:
        console.log("Month is March");
        break;
    case 4:
        console.log("Month is April");
        break;

    default:
        console.log("Default case match");
        break;
}



